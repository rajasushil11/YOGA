					<p><strong>Kriya Yoga</strong> – Working inside of the asana to circulate prana from one chakra to the next, a process of purification.</p>
					<p align="justify"><strong>Shiva Shakti Yoga</strong> – Requiring the use of all the yoga systems learned previously; it is our goal to make you a master of your own body, mind and energy systems. You will learn how to become an embodiment of divinity and to balance your own internal masculine and feminine energies.Harmonizing sexuality with spirituality to understand sacred sex. Ritual and Ceremony to celebrate and honor the divine feminine and noble masculine.</p>
