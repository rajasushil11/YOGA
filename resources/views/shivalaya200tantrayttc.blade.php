					<h3>OVERVIEW</h3>
					<p>Why Tantra?</p>
					<p>Tantra is the natural evolution of yoga. It is an integral system that consists of 5 Yogas, as originally taught by Lord Shiva, the original Guru of Yoga.</p>
					<p>Tan means expansion, tra means freedom. Tantra is the art of being free from all physical, mental and spiritual bondage through the expansion of energy.</p>
					<p>Unlike other systems of yoga, Tantra takes a liberal approach of embracing life in all its facets and using life as our ultimate teacher. Through accepting all that is, we find ourselves gradually liberated from all limitations and conditioning, which allows us to step fully into our personal power and the joy of being fully alive.</p>
					<p>Tantra Yoga emphasizes the development of power latent in the chakras in a systematic manner to heal and balances all aspects of the self – including the physical, emotional, vital, mental, and spiritual bodies. It eventually leads to supreme consciousness, the ultimate aim of human life.</p>

					<h3>OUR LINEAGE</h3>
					<p>Our school of Tantra Yoga comes out of Shanti Yoga Ashram of Nepal, whose lineage is the combination of Tantra Yoga as taught by Swami Nirajananda, successor of Swami Satyananda of Bihar School of Yoga and Swami Shivalingam of Nepal, also a student of Swami Satyananda, who taught the ancient hidden secrets of Nepalese Tantra to his disciple Yogi Prakash, or Guruji, as he is known, founder of Shanti Yoga Ashram.</p>
					<p>Nepal is the land of Tantra, where Shiva himself taught this science to his other half, Parvati. It has been preserved in its pure form from Guru to disciple ever since. It is our intention to transmit this wealth of knowledge, through adapted practices and providing a good structural means for conducive learning and integration.</p>

