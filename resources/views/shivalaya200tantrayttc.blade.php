					<h3>WHAT DO WE TEACH</h3>
					<p align="justify">Our full-time training stretches over a 4-week period. Teaching hours are Monday to Saturday, 06:30. Days start with meditation and Hatha Yoga practice, followed by breakfast and the morning theory block. The lunch breaks allow for rest and study. The afternoon sessions contain a short workshop followed by a practice session where you get the chance to practice teaching with a partner or in small groups. Your teachers will be available for questions and corrections. The first half of the training builds up a solid foundation of theory and practice, and the second half focuses on the development of teaching skills as well as deepening your spiritual practices. The curriculum of our 1-month intensive course is structured in the following way:</p>
					<p><strong>● Week 1: Hatha Yoga</strong><br>
						Basic asanas, breathing exercises, meditation, practical philosophy, diet, and introductory teaching practicum.</p>
					<p><strong>● Week 2: Astanga Yoga, Ayurveda</strong><br>
							Practice of intermediate asanas and pranayama, correction and adjustment training, Yoga Sutras of Patanjali, Bhakti Yoga based on Bhagavad Gita, mid-term exam at the end of the week, own teaching of beginner asanas.</p>
					<p><strong>● Week 3: Mantra Yoga, Energetic Principles</strong><br>
							Advanced practices of purification, Yantra and Mantra, Yoga anatomy, Intuitive Massage.</p>
					<p><strong>● Week 4: Kundalini and Shiva Shakti Yoga</strong><br>
								Chakra practices, Tantra meditations, final exam, teaching of a final 90-minute yoga class.&nbsp;Please note that you will receive the full syllabus on the first day of class. Every training course is slightly different as we adjust to the needs and learning level of our students. Tantra is an integrative system, so there will be elements of all the topics throughout the entire course. Please remain flexible.</p>
					<p><strong>WHAT DO WE TEACH? TANTRA’S 5 YOGA SYSTEM</strong></p>
								<p align="justify">Tantra yoga starts with the gross level, the body, and gradually transitions to the subtle level, consciousness. This teaching system offers the evolutionary process from Hatha yoga, meditation through the body, to Astanga yoga, meditation through the mind, to Mantra yoga, meditation through sound, to Kundalini yoga, meditation through the chakras, and ultimately to Shiva-Shakti yoga, meditation through the internal masculine and feminine energies.</p>
					<p align="justify"><strong>Hatha Yoga</strong> – The Foundation of Tantra. Classical Hatha Yoga to balance our solar and lunar energies, when the body is balanced in this way the energy can flow properly which is the basis of good health.</p>
					<p align="justify"><strong>Shat Karma</strong> – 1st step in Hatha Yoga, internal yogic cleansing to prepare our bodies and minds for the yoga practice to follow, so that when prana does start to flow it will be moving through clear open channels.</p>
					<p><strong>Asana, Pranayama, Mudra, Bhandha</strong></p>
					<p align="justify"><strong>Asana </strong>– 36 Classic Hatha Yoga Asanas, Sun Salutations with deeper esoteric meaning while incorporating chakra awareness, How to incorporate breathe, awareness and movement along with enjoyment.</p>
					<p><strong>Pranayama </strong>– Sensitizing, Vitalizing, Balancing and Tranquilizing. You will learn more than techniques but gain the ability to use your breathe to direct your own pranic flow.<br>
					<strong>Mudra </strong>– Create a flow of prana through different gestures, essential element of tantra yoga.<br>
					<strong>Bhanda </strong>– Learn to lock and release different parts of the body to direct the energy as needed.</p>
					<p><strong>Astanga Yoga (Raja Yoga) – 8 Limbs</strong>. There are practices in each of these limbs that help one evolve to the next.<br>
					<strong>Yama and Niyam</strong>a – Development of personal character by imbibing certain lifestyle regulations for upliftment. Bhakti Yoga and Karma Yoga are included here<br>
					<strong>Bhakti Yoga (Yoga of Devotion)</strong> – Embodying Divine Qualities.<br>
					<strong>Karma Yoga (Yoga of Selfless Service)</strong> – Doing something good for humanity that reaches beyond your own borders.<br>
					<strong>Asana </strong>– Become comfortable in your own skin and still so that the cosmic life-force may flow through you.<br>
					<strong>Pranayama </strong>– Learn to expand the space between the breaths to come fully into present moment awareness, preliminary stages of meditation.<br>
					<strong>Pratyahara </strong>– Can you fully relax and go within? Learn to withdraw your senses and find your true home, inside of you.<br>
					Yoga Nidra and other techniques will be practiced regularly</p>
					<p><strong>Dharana </strong>– Concentration – Esoteric Practices to create the strength and focus necessary for meditation<br>
					<strong>Dhyayna </strong>– Meditation will happen naturally with the preparatory practices we will give you.<br>
					<strong>Samadhi </strong>– Blissful state of Enlightenment – A glimpse will be possible as the soul begins to shine out and feel real peace.</p>
					<p><strong>Mantra Yoga</strong> – Once the body and mind are balanced we are ready to invoke the divine energy through sound vibration, the most subtle of all manifestations.<br>
					<strong>AUM Chanting</strong> – There are many ways to chant Om to awaken different energy centers and to either elevate or ground.<br>
					<strong>Kirtan</strong> – Singing together chants to invite Cosmic Energies.<br>
					<strong>Mantra Initiation</strong> – You will receive a mantra to connect you with the yoga lineage to chant on your own.<br>
